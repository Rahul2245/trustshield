from pydantic import BaseModel, Field, ConfigDict


class ShadowResult(BaseModel):
    """
    Represents the structured output returned from the Ollama LLM 
    acting as the shadow queue evaluator.
    """
    model_config = ConfigDict(frozen=True)

    is_malicious: bool = Field(
        ..., 
        description="Boolean flag indicating if the LLM deemed the payload malicious."
    )
    confidence_score: float = Field(
        ..., 
        ge=0.0, 
        le=1.0, 
        description="The model's confidence in its verdict, bounded between 0.0 and 1.0."
    )
    reason: str = Field(
        ..., 
        min_length=1,
        description="A brief explanation generated by the LLM justifying the verdict."
    ) 